import java.util.*;
public class PrintTheFollowingPattern{
   public static void main(String args[]){
       int size=new Scanner(System.in).nextInt();
       int front=size-1,back=0;
       for(int i=0;i<size*size;i++){
           if(i%size==0){
              front=size-1;
              back=0;
           }
         for(int j=0;j<size;j++){
             System.out.print("-".repeat(front)+"* "+"-".repeat(back));
         }
           front++;
           back--;
         System.out.println();
       }
     System.exit(0);
   }
}
